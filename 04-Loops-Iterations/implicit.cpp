#include <iostream>
using namespace std;

int main () {

    for ( char i = 'A' ; i <= 'Z' ; i++ ) {

        int value =  i ;

        cout << i << " ASCII value - " << value << endl;

    }

    for ( char i = 'a' ; i <= 'z' ; i++ ) {

        int value =  i ;

        cout << i << " ASCII value - " << value << endl;

    }

    return 0;

}
